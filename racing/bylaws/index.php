            <h4 class="text-center"><em>PREAMBLE</em></h4>
            <p class="text-justify">The IEEE Racing Team exists as a committee under the Purdue IEEE Student Branch. The
                mission of the IEEE Racing Team is to foster technical and professional skills of its members by
                designing, manufacturing, and testing electric vehicles in order to compete in the EV Grand Prix
                Competition and any other Racing events that the members decide relevant.</p>

            <br><h4 class="text-center"><em>Article I. Membership and Leadership</em></h4>
            <p class="text-justify">The IEEE Racing team, at minimum, will consist of a IEEE Committee Chair (henceforth
                referred to as captain), Learning Chair, Social Chair, Professional Chair, and Technical Lead. The
                captain may add or subtract any other leadership role he or she deems necessary. The captain is
                responsible for organizing the team, setting a technical direction, and making all decisions that best
                achieve the purpose of the IEEE Racing Team. The captain will be the central point of contact between
                the team and the rest of Purdue IEEE Student Branch. The other appointed chairs are responsible for
                meeting requirements set by their respective cornerstone committee and includes going to meetings and
                communicating on behalf of IEEE Racing. </p>

            <p class="text-justify"> Attendance at a majority of team meetings and continuous participation with
                meaningful contributions throughout the design, construction, and testing phases of IEEE Racing are
                expected of all members. Attendance at a majority of technical team meetings to which the respective
                member belongs is expected.  All members must attend at least 50% of all meetings and may not miss more
                than three (3) meetings in a row unless explicit written permission is given by the captain.
                Additionally, in order to be an official member of IEEE and any other committee under the Purdue IEEE
                Student Branch you must pay the IEEE local due. Infrequent attendance, little or no contributions made,
                disruptive behavior, or lack of IEEE dues payment may be grounds to not be recognized as a Racing
                member. </p>

            <br><h4 class="text-center"><em>Article II. Decisions</em></h4>
            <p class="text-justify">While decisions should usually be made with the advice and consent of the team, the
                captain shall have the ability to make or delegate all final team decisions. However, if there is a
                disagreement, a unanimous vote of leadership shall result in a vote over the decision being taken by the
                whole team (does not include the captain). A majority of voting team members will override any captain’s
                decision. </p>
            <p class="text-justify">The captain shall have the power to decide access to team resources in a manner
                consistent with these bylaws. Additionally, the captain will be responsible for the approval of team
                purchases. This process may be delegated by the captain as needed. Only with approval will a purchase be
                eligible for reimbursement by the IEEE Treasurer. The captain will be responsible for delivering the
                IEEE Racing budget for the season to the IEEE Treasurer within the first week of the academic year. </p>
            <p class="text-justify">While members of the team may be removed or demoted by the captain, the captain may
                lose his or her position in the same voting procedure listed in Article III, Paragraph I.</p>
            <p class="text-justify">In the event of a temporary captain vacancy, the captain may appoint an interim
                replacement in his or her absence. If the vacancy is longer than one month, an election should occur as
                soon as reasonably possible in the same way as defined below. The captain may appoint an acting captain
                for any length of time during his or her captaincy with a majority approval of the leadership.</p>

            <br><h4 class="text-center"><em>Article III. Elections</em></h4>
            <p class="text-justify">BBy May 1st of the current year, there shall be a vote for captain, with the elected
                captain assuming the position on June 1st. A captain must have been a member of the team for at least
                four months. All current members of the team at the time of the election shall have one vote.
                Eligibility for voting shall be decided by the captain with regards to the aforementioned bylaws. A
                majority of voting team members shall elect the captain. In the event no majority exists, the member
                with the fewest votes shall be removed from the ballot and another vote will occur. If there is still no
                majority when there are only two members left, the previous captain shall decide the new captain. Only
                when fewer than four members are present at elections will the captain at the time make the decision for
                next captain that is in the best interest for IEEE Racing and Purdue IEEE as a whole.</p>

            <br><h4 class="text-center"><em>Article IV. Bylaws</em></h4>
            <p class="text-justify">Any amendments to the bylaws are to be proposed and will be subject to approval by
                the captain. The bylaws shall be implemented with a majority of voting team members.</p>
