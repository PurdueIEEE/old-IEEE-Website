                <h1 class="text-center">Remotely Operated underwater Vehicle (ROV) Team Bylaws</h1></br>

                <h4 class="text-center"><em>PREAMBLE</em></h4>
                <p class="text-justify">The Purdue IEEE Remotely Operated underwater Vehicle (ROV) Team exists as a committee under the Purdue IEEE Student Organization. The mission of the ROV team is to foster technical and professional skills of its members by designing, constructing, and testing an innovative underwater vehicle to compete in the MATE Center International ROV Competition. All ROV team members are required to follow the rules established in these bylaws in addition to the IEEE Constitution.</p>

                <br><h4 class="text-center"><em>Structure and Membership</em></h4>
                <p class="text-justify">The ROV team, at minimum, shall be composed of a captain (who shall be IEEE committee chair), electrical team lead, mechanical team lead, and software team lead. Other, non-team lead positions may be appointed and removed at the captain’s leisure.</p>
                <p class="text-justify">Attendance at a majority of team meetings and continuous participation with meaningful contributions throughout the design, construction, testing, and documentation phases of the ROV are expected of all members. Additionally, attendance at a majority of technical team meetings to which the respective member belongs is expected. Infrequent attendance, low number or quality of contributions made, disruptive behavior, or lack of IEEE dues payment may be cited as reasons to not be recognized as a member, selected to represent the team at outreach events, or allowed to attend competitions.</p>

                <br><h4 class="text-center"><em>Decisions</em></h4>
                <p class="text-justify">While decisions should usually be made with the advice and consent of the team, the captain shall have the ability to make or delegate all final team decisions. However, if there is a disagreement, a unanimous vote of team leads shall result in a vote over the decision being taken by the whole team. A majority of voting team members will override any captain’s decision.</p>
                <p class="text-justify">The captain shall have the power to decide access to team resources in a manner consistent with these bylaws. Additionally, the captain will be responsible for the approval of team purchases. This process may be delegated by the captain as needed. Only with approval will a purchase be eligible for reimbursement by the IEEE Treasurer.</p>
                <p class="text-justify">While members of the team may be removed or demoted by the captain, the captain may lose his or her position only in the same voting procedure listed above. </p>
                <p class="text-justify">In the event of a temporary captain vacancy he or she may appoint a captain in his or her absence. If the vacancy is longer than one month, an election should occur as soon as reasonably possible in the same way as defined below. The captain may appoint an acting captain for any length of time during his or her captaincy with a majority approval of the team leads.</p>

                <br><h4 class="text-center"><em>Elections</em></h4>
                <p class="text-justify">Before the beginning of each new season, there shall be a vote for captain. A season shall consist of no more than one international competition. A captain must have been a member of the team for at least four months. All current members of the team at the time of the election shall have one vote. Eligibility for voting shall be decided by the captain with regards to the aforementioned bylaws. A majority of voting team members shall elect the captain. In the event no majority exists, the member with the fewest votes shall be removed from the ballet and another vote will occur. If there is still no majority when there are only two members left, the previous captain shall decide the new captain.</p>

                <br><h4 class="text-center"><em>Bylaws</em></h4>
                <p class="text-justify">Any amendments to the bylaws shall be proposed and approved by the captain. The bylaws shall pass with a majority of voting team members or by a unanimous team lead vote.</p>
